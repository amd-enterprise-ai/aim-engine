func TestPlanClusterModelServiceTemplates_CreateTemplates(t *testing.T) {
	templateObs := clusterModelServiceTemplateObservation{
		shouldCreateTemplates: true,
	}
	metadataObs := modelMetadataObservation{
		ExtractedMetadata: &aimv1alpha1.ImageMetadata{
			Model: &aimv1alpha1.ModelMetadata{
				RecommendedDeployments: []aimv1alpha1.RecommendedDeployment{
					{
						Metric:    "throughput",
						Precision: "fp16",
						GPUModel:  defaultGpuModel,
						GPUCount:  1,
					},
					{
						Metric:    "latency",
						Precision: "fp32",
						GPUModel:  defaultGpuModel,
						GPUCount:  2,
					},
				},
			},
		},
	}
	clusterModel := aimv1alpha1.AIMClusterModel{
		Spec: aimv1alpha1.AIMModelSpec{
			Image: "test:latest",
		},
	}

	templates := planClusterModelServiceTemplates(templateObs, metadataObs, clusterModel)

	if len(templates) != 2 {
		t.Errorf("expected 2 templates, got %d", len(templates))
	}

	for _, tmpl := range templates {
		clusterTemplate, ok := tmpl.(*aimv1alpha1.AIMClusterServiceTemplate)
		if !ok {
			t.Fatal("expected AIMClusterServiceTemplate")
		}
		if clusterTemplate.Name == "" {
			t.Error("expected template name to be set")
		}
		if clusterTemplate.Labels[constants.LabelKeyAutoGenerated] != constants.LabelValueAutoGenerated {
			t.Error("expected auto-generated label")
		}
	}
}

