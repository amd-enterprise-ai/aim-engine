// ============================================================================
// BUILD TEMPLATE COMPONENT TESTS
// ============================================================================

func TestBuildTemplateComponents(t *testing.T) {
	modelSpec := aimv1alpha1.AIMModelSpec{
		Image: "test-image:v1",
	}
	deployment := aimv1alpha1.RecommendedDeployment{
		Metric:    "throughput",
		Precision: "fp16",
		GPUModel:  testGPUModel,
		GPUCount:  2,
	}

	result := buildTemplateComponents(testModelName, modelSpec, deployment)

	if result.Name == "" {
		t.Error("expected template name to be set")
	}

	if result.Spec.ModelName != testModelName {
		t.Errorf("expected ModelName=%s, got %s", testModelName, result.Spec.ModelName)
	}

	if result.Spec.Metric == nil || string(*result.Spec.Metric) != "throughput" {
		t.Error("expected Metric=throughput")
	}

	if result.Spec.Precision == nil || string(*result.Spec.Precision) != "fp16" {
		t.Error("expected Precision=fp16")
	}

	if result.Spec.Gpu == nil {
		t.Fatal("expected Gpu to be set")
	}
	if result.Spec.Gpu.Model != testGPUModel {
		t.Errorf("expected Gpu.Model=%s, got %v", testGPUModel, result.Spec.Gpu.Model)
	}
	if result.Spec.Gpu.Requests != 2 {
		t.Errorf("expected Gpu.Requests=2, got %d", result.Spec.Gpu.Requests)
	}

	if result.Labels[constants.LabelKeyOrigin] != constants.LabelValueOriginAutoGenerated {
		t.Error("expected origin=auto-generated label")
	}
	if result.Labels[constants.LabelKeyModel] != testModelName {
		t.Error("expected model label")
	}
}

