func TestShouldCreateTemplates(t *testing.T) {
	tests := []struct {
		name      string
		modelSpec aimv1alpha1.AIMModelSpec
		config    *aimv1alpha1.AIMRuntimeConfigCommon
		expected  bool
	}{
		{
			name: "default - should create templates",
			modelSpec: aimv1alpha1.AIMModelSpec{
				Image: "test:latest",
			},
			config:   nil,
			expected: true,
		},
		{
			name: "auto discovery disabled in config",
			modelSpec: aimv1alpha1.AIMModelSpec{
				Image: "test:latest",
			},
			config: &aimv1alpha1.AIMRuntimeConfigCommon{
				Model: &aimv1alpha1.AIMModelConfig{
					AutoDiscovery: ptr.To(false),
				},
			},
			expected: false,
		},
		{
			name: "auto discovery enabled in config",
			modelSpec: aimv1alpha1.AIMModelSpec{
				Image: "test:latest",
			},
			config: &aimv1alpha1.AIMRuntimeConfigCommon{
				Model: &aimv1alpha1.AIMModelConfig{
					AutoDiscovery: ptr.To(true),
				},
			},
			expected: true,
		},
		{
			name: "autoCreateTemplates disabled in model spec",
			modelSpec: aimv1alpha1.AIMModelSpec{
				Image: "test:latest",
				Discovery: &aimv1alpha1.AIMModelDiscoveryConfig{
					AutoCreateTemplates: ptr.To(false),
				},
			},
			config:   nil,
			expected: false,
		},
		{
			name: "autoCreateTemplates enabled in model spec",
			modelSpec: aimv1alpha1.AIMModelSpec{
				Image: "test:latest",
				Discovery: &aimv1alpha1.AIMModelDiscoveryConfig{
					AutoCreateTemplates: ptr.To(true),
				},
			},
			config:   nil,
			expected: true,
		},
		{
			name: "model spec overrides config",
			modelSpec: aimv1alpha1.AIMModelSpec{
				Image: "test:latest",
				Discovery: &aimv1alpha1.AIMModelDiscoveryConfig{
					AutoCreateTemplates: ptr.To(false),
				},
			},
			config: &aimv1alpha1.AIMRuntimeConfigCommon{
				Model: &aimv1alpha1.AIMModelConfig{
					AutoDiscovery: ptr.To(true),
				},
			},
			expected: false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result := shouldCreateTemplates(tt.modelSpec, tt.config)
			if result != tt.expected {
				t.Errorf("shouldCreateTemplates() = %v, want %v", result, tt.expected)
			}
		})
	}
}

func TestBuildTemplateComponents(t *testing.T) {
	modelSpec := aimv1alpha1.AIMModelSpec{
		Image: "test-image:v1",
	}
	deployment := aimv1alpha1.RecommendedDeployment{
		Metric:    "throughput",
		Precision: "fp16",
		GPUModel:  defaultGpuModel,
		GPUCount:  2,
	}

	result := buildTemplateComponents("test-model", modelSpec, deployment)

	if result.Name == "" {
		t.Error("expected template name to be set")
	}

	if result.Spec.ModelName != "test-model" {
		t.Errorf("expected ModelName=test-model, got %s", result.Spec.ModelName)
	}

	if result.Spec.Metric == nil || string(*result.Spec.Metric) != "throughput" {
		t.Error("expected Metric=throughput")
	}

	if result.Spec.Precision == nil || string(*result.Spec.Precision) != "fp16" {
		t.Error("expected Precision=fp16")
	}

	if result.Spec.GpuSelector == nil {
		t.Fatal("expected GpuSelector to be set")
	}
	if result.Spec.GpuSelector.Model != defaultGpuModel {
		t.Errorf("expected GpuSelector.Model=MI300X, got %s", result.Spec.GpuSelector.Model)
	}
	if result.Spec.GpuSelector.Count != 2 {
		t.Errorf("expected GpuSelector.Count=2, got %d", result.Spec.GpuSelector.Count)
	}

	if result.Labels[constants.LabelKeyAutoGenerated] != constants.LabelValueAutoGenerated {
		t.Error("expected auto-generated label")
	}
	if result.Labels[constants.LabelKeyModelName] != "test-model" {
		t.Error("expected model name label")
	}
}

