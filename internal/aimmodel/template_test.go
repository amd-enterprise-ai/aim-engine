func TestBuildServiceTemplate_MultipleDeployments(t *testing.T) {
	model := &aimv1alpha1.AIMModel{
		Spec: aimv1alpha1.AIMModelSpec{
			Image: "test:latest",
		},
	}
	model.Name = testModelName
	model.Namespace = testNamespace

	deployments := []aimv1alpha1.RecommendedDeployment{
		{
			Metric:    "throughput",
			Precision: "fp16",
			GPUModel:  testGPUModel,
			GPUCount:  1,
		},
		{
			Metric:    "latency",
			Precision: "fp32",
			GPUModel:  testGPUModel,
			GPUCount:  2,
		},
	}

	// Build templates for each deployment
	for i, deployment := range deployments {
		template := buildServiceTemplate(model, deployment)

		if template.Name == "" {
			t.Errorf("template %d: expected name to be set", i)
		}
		if template.Spec.Metric == nil {
			t.Errorf("template %d: expected Metric to be set", i)
		}
		if template.Spec.Precision == nil {
			t.Errorf("template %d: expected Precision to be set", i)
		}
		if template.Spec.Gpu == nil || template.Spec.Gpu.Requests != deployment.GPUCount {
			t.Errorf("template %d: expected GPU count=%d", i, deployment.GPUCount)
		}
	}
}
