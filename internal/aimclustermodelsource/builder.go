package aimclustermodelsource

import (
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"

	aimv1alpha1 "github.com/amd-enterprise-ai/aim-engine/api/v1alpha1"
	"github.com/amd-enterprise-ai/aim-engine/internal/constants"
	"github.com/amd-enterprise-ai/aim-engine/internal/utils"
)

const (
	// LabelKeyModelSource is the label key used to identify the model source that created a cluster model.
	LabelKeyModelSource = "aim.eai.amd.com/model-source"
)

// buildClusterModel creates an AIMClusterModel from a source and discovered image.
func buildClusterModel(
	source *aimv1alpha1.AIMClusterModelSource,
	img RegistryImage,
) *aimv1alpha1.AIMClusterModel {
	return &aimv1alpha1.AIMClusterModel{
		ObjectMeta: metav1.ObjectMeta{
			Name: generateModelName(img),
			Labels: map[string]string{
				LabelKeyModelSource:      source.Name,
				constants.LabelKeyOrigin: constants.LabelValueOriginAutoGenerated,
			},
			Annotations: map[string]string{
				"aim.eai.amd.com/source-registry":   img.Registry,
				"aim.eai.amd.com/source-repository": img.Repository,
				"aim.eai.amd.com/source-tag":        img.Tag,
			},
		},
		Spec: aimv1alpha1.AIMModelSpec{
			Image:            img.ToImageURI(),
			ImagePullSecrets: source.Spec.ImagePullSecrets,
		},
	}
}

// generateModelName creates a Kubernetes-valid name from a registry image.
// It uses the repository and tag as name parts, with registry, repository, and tag
// as hash inputs for deterministic uniqueness.
//
// Format: <repository>-<tag>-<hash>
//
// Examples:
//
//	{Registry: "ghcr.io", Repository: "silogen/llama-3-8b", Tag: "v1.2.0"} -> silogen-llama-3-8b-v1-2-0-a1b2c3
//	{Registry: "docker.io", Repository: "library/nginx", Tag: "latest"} -> library-nginx-latest-e5f6g7
func generateModelName(img RegistryImage) string {
	// Use GenerateDerivedName with repository and tag as name parts
	// and registry, repository, tag as hash inputs for uniqueness
	name, _ := utils.GenerateDerivedName(
		[]string{img.Repository, img.Tag},
		utils.WithHashSource(img.Registry, img.Repository, img.Tag), // Hash inputs for determinism
		utils.WithHashLength(6),                                     // 6-char hash for collision avoidance
	)
	return name
}
